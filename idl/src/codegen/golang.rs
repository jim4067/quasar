/// Returns `true` if the type is a top-level dynamic type (`DynString` or
/// `DynVec`). These require compact 3-phase encoding at the instruction level.
fn is_direct_dynamic(ty: &IdlType) -> bool {
    matches!(ty, IdlType::DynString { .. } | IdlType::DynVec { .. })
}

fn serialize_field_expr(name: &str, ty: &IdlType, types: &[IdlTypeDef]) -> String {
    match ty {
        IdlType::Primitive(p) => match p.as_str() {
            "bool" => format!(
                "\tif input.{n} {{\n\t\tdata = append(data, 1)\n\t}} else {{\n\t\tdata = \
                 append(data, 0)\n\t}}\n",
                n = name,
            ),
            "u8" => format!("\tdata = append(data, input.{})\n", name),
            "i8" => format!("\tdata = append(data, byte(input.{}))\n", name),
            "u16" => format!(
                "\t{{ var buf [2]byte; binary.LittleEndian.PutUint16(buf[:], input.{n}); data = \
                 append(data, buf[:]...) }}\n",
                n = name,
            ),
            "i16" => format!(
                "\t{{ var buf [2]byte; binary.LittleEndian.PutUint16(buf[:], uint16(input.{n})); \
                 data = append(data, buf[:]...) }}\n",
                n = name,
            ),
            "u32" => format!(
                "\t{{ var buf [4]byte; binary.LittleEndian.PutUint32(buf[:], input.{n}); data = \
                 append(data, buf[:]...) }}\n",
                n = name,
            ),
            "i32" => format!(
                "\t{{ var buf [4]byte; binary.LittleEndian.PutUint32(buf[:], uint32(input.{n})); \
                 data = append(data, buf[:]...) }}\n",
                n = name,
            ),
            "u64" => format!(
                "\t{{ var buf [8]byte; binary.LittleEndian.PutUint64(buf[:], input.{n}); data = \
                 append(data, buf[:]...) }}\n",
                n = name,
            ),
            "i64" => format!(
                "\t{{ var buf [8]byte; binary.LittleEndian.PutUint64(buf[:], uint64(input.{n})); \
                 data = append(data, buf[:]...) }}\n",
                n = name,
            ),
            "u128" | "i128" => format!("\tdata = append(data, input.{}[:]...)\n", name,),
            "f32" => format!(
                "\t{{ var buf [4]byte; binary.LittleEndian.PutUint32(buf[:], \
                 math.Float32bits(input.{n})); data = append(data, buf[:]...) }}\n",
                n = name,
            ),
            "f64" => format!(
                "\t{{ var buf [8]byte; binary.LittleEndian.PutUint64(buf[:], \
                 math.Float64bits(input.{n})); data = append(data, buf[:]...) }}\n",
                n = name,
            ),
            "pubkey" => format!("\tdata = append(data, input.{}[:]...)\n", name,),
            "string" => format!(
                "\t{{ b := []byte(input.{n}); var buf [4]byte; \
                 binary.LittleEndian.PutUint32(buf[:], uint32(len(b))); data = append(data, \
                 buf[:]...); data = append(data, b...) }}\n",
                n = name,
            ),
            _ if p.starts_with('[') => {
                format!("\tdata = append(data, input.{}[:]...)\n", name)
            }
            _ => format!("\tdata = append(data, input.{}...)\n", name),
        },
        IdlType::DynString { ref string } => match string.prefix_bytes {
            1 => format!(
                "\tdata = append(data, byte(len([]byte(input.{n}))))\n\tdata = append(data, \
                 []byte(input.{n})...)\n",
                n = name,
            ),
            2 => format!(
                "\t{{ b := []byte(input.{n}); var buf [2]byte; \
                 binary.LittleEndian.PutUint16(buf[:], uint16(len(b))); data = append(data, \
                 buf[:]...); data = append(data, b...) }}\n",
                n = name,
            ),
            4 => format!(
                "\t{{ b := []byte(input.{n}); var buf [4]byte; \
                 binary.LittleEndian.PutUint32(buf[:], uint32(len(b))); data = append(data, \
                 buf[:]...); data = append(data, b...) }}\n",
                n = name,
            ),
            _ => format!(
                "\t{{ b := []byte(input.{n}); var buf [8]byte; \
                 binary.LittleEndian.PutUint64(buf[:], uint64(len(b))); data = append(data, \
                 buf[:]...); data = append(data, b...) }}\n",
                n = name,
            ),
        },
        IdlType::Option { option } => {
            let inner = serialize_field_expr(&format!("(*input.{})", name), option, types);
            format!(
                "\tif input.{n} == nil {{\n\t\tdata = append(data, 0)\n\t}} else {{\n\t\tdata = \
                 append(data, 1)\n{inner}\t}}\n",
                n = name,
                inner = inner,
            )
        }
        IdlType::Defined { defined } => {
            if let Some(td) = types.iter().find(|t| t.name == *defined) {
                let mut result = String::new();
                for field in &td.ty.fields {
                    result.push_str(&serialize_field_expr(
                        &format!("{}.{}", name, snake_to_pascal(&field.name)),
                        &field.ty,
                        types,
                    ));
                }
                result
            } else {
                format!("\tdata = append(data, input.{}...)\n", name)
            }
        }
        IdlType::DynVec { vec } => match vec.prefix_bytes {
            1 => format!(
                "\tdata = append(data, byte(len(input.{n})))\n\tdata = append(data, \
                 input.{n}...)\n",
                n = name,
            ),
            2 => format!(
                "\t{{ var buf [2]byte; binary.LittleEndian.PutUint16(buf[:], \
                 uint16(len(input.{n}))); data = append(data, buf[:]...); data = append(data, \
                 input.{n}...) }}\n",
                n = name,
            ),
            4 => format!(
                "\t{{ var buf [4]byte; binary.LittleEndian.PutUint32(buf[:], \
                 uint32(len(input.{n}))); data = append(data, buf[:]...); data = append(data, \
                 input.{n}...) }}\n",
                n = name,
            ),
            _ => format!(
                "\t{{ var buf [8]byte; binary.LittleEndian.PutUint64(buf[:], \
                 uint64(len(input.{n}))); data = append(data, buf[:]...); data = append(data, \
                 input.{n}...) }}\n",
                n = name,
            ),
        },
    }
}

/// Go float decode expression. Extracted to prevent rustfmt from splitting
/// the `\n` in the format string across a line continuation.
fn go_float_decode(t: &str, n: &str, math_fn: &str, le_fn: &str, size: usize) -> String {
    format!(
        "{t}{n} := math.{math_fn}(binary.LittleEndian.{le_fn}(data[offset:]))\n{t}offset += \
         {size}\n"
    )
}

// ---------------------------------------------------------------------------
// Deserialization
// ---------------------------------------------------------------------------

fn decode_field_expr(name: &str, ty: &IdlType, depth: usize, types: &[IdlTypeDef]) -> String {
    let t = "\t".repeat(depth);
    match ty {
        IdlType::Primitive(p) => match p.as_str() {
            "bool" => format!(
                "{t}{n} := data[offset] != 0\n{t}offset += 1\n",
                t = t,
                n = name,
            ),
            "u8" => format!("{t}{n} := data[offset]\n{t}offset += 1\n", t = t, n = name,),
            "i8" => format!(
                "{t}{n} := int8(data[offset])\n{t}offset += 1\n",
                t = t,
                n = name,
            ),
            "u16" => format!(
                "{t}{n} := binary.LittleEndian.Uint16(data[offset:])\n{t}offset += 2\n",
                t = t,
                n = name,
            ),
            "i16" => format!(
                "{t}{n} := int16(binary.LittleEndian.Uint16(data[offset:]))\n{t}offset += 2\n",
                t = t,
                n = name,
            ),
            "u32" => format!(
                "{t}{n} := binary.LittleEndian.Uint32(data[offset:])\n{t}offset += 4\n",
                t = t,
                n = name,
            ),
            "i32" => format!(
                "{t}{n} := int32(binary.LittleEndian.Uint32(data[offset:]))\n{t}offset += 4\n",
                t = t,
                n = name,
            ),
            "u64" => format!(
                "{t}{n} := binary.LittleEndian.Uint64(data[offset:])\n{t}offset += 8\n",
                t = t,
                n = name,
            ),
            "i64" => format!(
                "{t}{n} := int64(binary.LittleEndian.Uint64(data[offset:]))\n{t}offset += 8\n",
                t = t,
                n = name,
            ),
            "u128" | "i128" => format!(
                "{t}var {n} [16]byte\n{t}copy({n}[:], data[offset:offset+16])\n{t}offset += 16\n",
                t = t,
                n = name,
            ),
            "f32" => go_float_decode(&t, name, "Float32frombits", "Uint32", 4),
            "f64" => go_float_decode(&t, name, "Float64frombits", "Uint64", 8),
            "pubkey" => format!(
                "{t}var {n} solana.PublicKey\n{t}copy({n}[:], data[offset:offset+32])\n{t}offset \
                 += 32\n",
                t = t,
                n = name,
            ),
            _ if p.starts_with('[') => {
                let size = super::parse_fixed_array_size(p).unwrap_or(1);
                format!(
                    "{t}var {n} [{sz}]byte\n{t}copy({n}[:], data[offset:offset+{sz}])\n{t}offset \
                     += {sz}\n",
                    t = t,
                    n = name,
                    sz = size,
                )
            }
            _ => format!(
                "{t}{n} := data[offset:] // unsupported type\n{t}_ = {n}\n",
                t = t,
                n = name,
            ),
        },
        IdlType::DynString { ref string } => match string.prefix_bytes {
            1 => format!(
                "{t}{n}Len := int(data[offset])\n{t}offset += 1\n{t}{n} := \
                 string(data[offset:offset+{n}Len])\n{t}offset += {n}Len\n",
                t = t,
                n = name,
            ),
            2 => format!(
                "{t}{n}Len := int(binary.LittleEndian.Uint16(data[offset:]))\n{t}offset += \
                 2\n{t}{n} := string(data[offset:offset+{n}Len])\n{t}offset += {n}Len\n",
                t = t,
                n = name,
            ),
            4 => format!(
                "{t}{n}Len := int(binary.LittleEndian.Uint32(data[offset:]))\n{t}offset += \
                 4\n{t}{n} := string(data[offset:offset+{n}Len])\n{t}offset += {n}Len\n",
                t = t,
                n = name,
            ),
            _ => format!(
                "{t}{n}Len := int(binary.LittleEndian.Uint64(data[offset:]))\n{t}offset += \
                 8\n{t}{n} := string(data[offset:offset+{n}Len])\n{t}offset += {n}Len\n",
                t = t,
                n = name,
            ),
        },
        IdlType::Option { option } => {
            let inner = decode_field_expr(&format!("{}_val", name), option, depth, types);
            format!(
                "{t}var {n} *{ty}\n{t}if data[offset] != 0 {{\n{t}\toffset += 1\n{inner}{t}\t{n} \
                 = &{n}_val\n{t}}} else {{\n{t}\toffset += 1\n{t}}}\n",
                t = t,
                n = name,
                ty = go_type(option),
                inner = inner,
            )
        }
        IdlType::Defined { defined } => {
            if let Some(td) = types.iter().find(|t| t.name == *defined) {
                let mut result = String::new();
                for field in &td.ty.fields {
                    result.push_str(&decode_field_expr(
                        &to_camel_case(&field.name),
                        &field.ty,
                        depth,
                        types,
                    ));
                }
                let field_assigns: Vec<String> = td
                    .ty
                    .fields
                    .iter()
                    .map(|f| format!("{}: {},", snake_to_pascal(&f.name), to_camel_case(&f.name)))
                    .collect();
                result.push_str(&format!(
                    "{t}{n} := {cls}{{\n",
                    t = t,
                    n = name,
                    cls = defined
                ));
                for a in &field_assigns {
                    result.push_str(&format!("{t}\t{a}\n", t = t, a = a));
                }
                result.push_str(&format!("{t}}}\n", t = t));
                result
            } else {
                format!(
                    "{t}{n} := data[offset:] // unknown type\n{t}_ = {n}\n",
                    t = t,
                    n = name,
                )
            }
        }
        IdlType::DynVec { vec } => match vec.prefix_bytes {
            1 => format!(
                "{t}{n}Len := int(data[offset])\n{t}offset += 1\n{t}{n} := \
                 data[offset:offset+{n}Len]\n{t}offset += {n}Len\n",
                t = t,
                n = name,
            ),
            2 => format!(
                "{t}{n}Len := int(binary.LittleEndian.Uint16(data[offset:]))\n{t}offset += \
                 2\n{t}{n} := data[offset:offset+{n}Len]\n{t}offset += {n}Len\n",
                t = t,
                n = name,
            ),
            4 => format!(
                "{t}{n}Len := int(binary.LittleEndian.Uint32(data[offset:]))\n{t}offset += \
                 4\n{t}{n} := data[offset:offset+{n}Len]\n{t}offset += {n}Len\n",
                t = t,
                n = name,
            ),
            _ => format!(
                "{t}{n}Len := int(binary.LittleEndian.Uint64(data[offset:]))\n{t}offset += \
                 8\n{t}{n} := data[offset:offset+{n}Len]\n{t}offset += {n}Len\n",
                t = t,
                n = name,
            ),
        },
    }
}
